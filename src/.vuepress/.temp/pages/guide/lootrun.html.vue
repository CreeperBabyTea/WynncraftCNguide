<h3 id="开始lootrun" tabindex="-1"><a class="header-anchor" href="#开始lootrun" aria-hidden="true">#</a> 开始lootrun</h3>
<p>与Lootrun Master对话后，你就可以开始新lootrun了</p>
<div class="hint-container warning">
<p class="hint-container-title">注意</p>
<p>在开始lr前，你一般需要完成前置条件</p>
<p>所有区域的前置条件，角色之间不共用</p>
<p>前置条件一般是跑遍这个区域的每一个Cave，以及完成特定前置任务</p>
</div>
<p>你可以在此看到最小挑战轮数以及最大挑战轮数还有基础挑战轮数。</p>
<figure><img src="/assets/img/lootrun8.jpg" alt="" tabindex="0" loading="lazy"><figcaption></figcaption></figure>
<p>每个账号在每天第一次lootrun完成时，都能获得每日奖励</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>在满级后，每日奖励包含+5pulls以及+1rr</p>
<p>注意：这仅适用于每日第一次<strong>成功结算</strong>的局数</p>
<p>这意味着，如果你今天已经吃了每日奖励，你仍然可以保持已经开始的游戏不结算，并等待至第二天每日奖励刷新时再进行结算，以此来吃到每日奖励</p>
<p>每日奖励的刷新时间是北京时间早上八点</p>
<p>特别地，如果你每日第一局失败了，不会消耗每日奖励</p>
</div>
<p>在开始游戏后，你可以看到计分板出现计时器以及轮次数</p>
<figure><img src="/assets/img/lootrun9.jpg" alt="" tabindex="0" loading="lazy"><figcaption></figcaption></figure>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>在开始游戏后，当你离线/切换其他存档时，你当前的游戏将会暂停</p>
<p>若你已经进入挑战，在上线后你当前的挑战将会重置</p>
<p>处于Spelunk挑战中时，洞穴内所有T3T4箱子将会同时重置</p>
</div>
<p>同时，你可以在任何时候找NPC结算(或者投降。)</p>
<figure><img src="/assets/img/lootrun12.jpg" alt="" tabindex="0" loading="lazy"><figcaption></figcaption></figure>
<p>结算效果等于直接放弃接下来所有挑战数</p>
<div class="hint-container warning">
<p class="hint-container-title">注意</p>
<p>只有在成功完成4轮挑战后，整局lootrun才算成功</p>
<p>在此之前若你失败过多次或找NPC直接结算，都会直接判Lootrun Failed且不会有任何奖励</p>
</div>
<div class="hint-container warning">
<p class="hint-container-title">注意</p>
<p>在lr期间，你不能跟除了铁匠、鉴定商、lr大师以外的npc交互</p>
<p>同时，你也无法骑马，只能通过正常跑步进行跑图</p>
<p>这意味着，你唯一存放身上物品的方式是通过使用RANK-CHAMPION的宠物运送功能</p>
<p><s>这真的不是骗氪吗</s></p>
</div>
<div class="hint-container danger">
<p class="hint-container-title">关于游玩区域限制</p>
<p>每个lootrun地区都有限制一定游玩区域</p>
<p>当你离开这个区域后，会显示<code v-pre>return to playable area in X second</code></p>
<p>千万不要以为这是提示，<strong>这是警告</strong></p>
<p>在倒计时结束后，你就会被<strong>强制结算</strong>并传送回camp</p>
<p>注意当你出现了这个提示并且限时内无法回去时，一定要使用<code v-pre>/kill</code>来回到camp</p>
<p>这个真的很重要！！！</p>
</div>
<h3 id="计时器" tabindex="-1"><a class="header-anchor" href="#计时器" aria-hidden="true">#</a> 计时器</h3>
<p>一旦计时器耗尽或者轮次数耗尽，你都会被立刻结算并且传送回Lootrun Camp</p>
<p>当挑战开始时，都会获得1min或2min的跑图补偿时间，视地区大小而定</p>
<p>当挑战成功后，都会获得2min的挑战补偿时间</p>
<p>若任何时候死亡或挑战失败，均会扣除1min的时间</p>
<p>总时间上限为15分钟，除了绿信标以外，其他任何正常方式均无法使计时器超过15分钟</p>
<p>在计时器超过15分钟时，进出挑战仅会获得1s的时间加成(以此来显示是否处于红信标的轮次)</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>计时器会在选择boon、选择任务、开启挑战最终宝箱时暂停计时</p>
</div>
