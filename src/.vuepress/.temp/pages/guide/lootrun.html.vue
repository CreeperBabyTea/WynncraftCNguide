<p>箱子等级可以在开箱界面左上角看到</p>
<p><strong>对于每一个箱子来说，刷新时间越久，箱子质量越高</strong></p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>刚刷新出来的箱子质量非常低</p>
<p>刷新时间是分个人而非共享</p>
</div>
<p>T1-T2箱子分布在野外，箱子数量多，但是箱子质量不是很高，追求一个走量</p>
