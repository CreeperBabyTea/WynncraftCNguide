<h3 id="最终奖励" tabindex="-1"><a class="header-anchor" href="#最终奖励" aria-hidden="true">#</a> 最终奖励</h3>
<p>在完成lootrun后，你可以开启最终奖励宝箱</p>
<p>对于每个区域的最终奖励宝箱，都会有每周刷新的神话、传奇等物品</p>
<p>每个区域的最终奖励宝箱都不同</p>
<p>最终奖励宝箱会根据你的最终奖励抽取物品数(以下简称pulls)来抽取物品</p>
<p>奖池刷新时间为北京时间每周六凌晨，冬令时为3am，夏令时为2am</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>总物品数上限为36</p>
<p>如果你的pulls超过了36，则会根据优先级截断优先级较低的物品，保留优先级较高的物品</p>
<p><strong>注意最终奖励宝箱的奖励抽取不受任何lb、lq的影响！</strong></p>
</div>
<h4 id="end-reward-reroll" tabindex="-1"><a class="header-anchor" href="#end-reward-reroll" aria-hidden="true">#</a> <strong>End Reward Reroll</strong></h4>
<p>简称rr</p>
<p>你可以消耗最终奖励刷新机会(End Reward Reroll)来刷新物品</p>
<p>rr相当于重抽一次，且不会保留你重抽前的任何物品</p>
<h4 id="end-reward-sacrifice" tabindex="-1"><a class="header-anchor" href="#end-reward-sacrifice" aria-hidden="true">#</a> <strong>End Reward Sacrifice</strong></h4>
<p>简称sac</p>
<p>在确认开启前，如果你通过任务获得了sac数，你可以随时选择献祭奖励(End Reward Sacrifice)</p>
<p>sac会放弃当前所有奖励，根据你的sac数来保留当前的一部分pulls至下一局</p>
<figure><img src="/assets/img/lootrun13.jpg" alt="" tabindex="0" loading="lazy"><figcaption></figcaption></figure>
<p>1sac为保存50%的pulls，2sac为保存75%的pulls</p>
<p>在确认开启后，你就不能进行任何操作了，包括rr和sac</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>若下一局failed了，sac所继承的pulls会继续保留，直到成功结算为止</p>
<p>sac所保存的pulls一样可以被下一局的sac一起继承</p>
</div>
<p>最终奖励分为如下几个种类：</p>
<ul>
<li>
<p>Shiny Mythic：特等奖，与普通Mythic的区别就是多了一个独特的计数器以及只有自己才能看到的附魔特效，没了。一个区域奖池只会有一件Shiny Mythic</p>
</li>
<li>
<p>Mythic：一等奖，通常来说，一个奖池会有4-6件Mythic品质的物品，同时还可能刷新Insulator和Simulator(鉴定道具)。获取概率稍次于Shiny Mythic</p>
</li>
<li>
<p>Liquid Emerald：二等奖，奖池内通常会有1-2种10个以上le的奖品以及2-3种1-4个le的奖品，非赌狗开到大数目le基本可以收手了</p>
</li>
<li>
<p>Lootrun Tome：三等奖，其只有lr能够产出，通常包含一定数值的lb以及各元素伤害、防御等，同样为角色灵魂绑定</p>
</li>
<li>
