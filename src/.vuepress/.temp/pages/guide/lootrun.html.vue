<p>1.Cleansing Ritual任务，见到就拿，可以让你无条件拖到100轮上限</p>
<p>2.红绿交替，非常坐牢，需要时刻注意不要浪费带时间的轮次数</p>
<p>这个阶段如果你选了Cleansing Ritual，就可以不用信标选数，反之你需要考虑一下你的青红绿信标的情况：</p>
<p>如果青红绿刷在同一轮，且下一轮一起消失，则你需要尽量降低你的信标选数，好打乱青红绿的规律</p>
