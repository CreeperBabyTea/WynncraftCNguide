<figure><img src="/assets/img/lootrun5.jpg" alt="" tabindex="0" loading="lazy"><figcaption></figcaption></figure>
<p><code v-pre>Loot Quality</code>能提高箱子的物品质量，可以理解为提高神话的出率，同时还能开到更多传奇物品。</p>
<p>就现有的材料来说，你无法在100级前正常获得<code v-pre>Loot Quality</code>属性</p>
