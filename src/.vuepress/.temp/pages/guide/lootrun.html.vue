<p>lootrun即跑箱，在一定的区域内寻找散落在各处的宝箱并开启，以此来获取装备，一般简称lr</p>
<p>通常情况下，lr的终极目标即获取稀有度最高的Mythic品质装备</p>
<p>大部分Mythic装备都拥有高于其他品质的属性以及价值</p>
<blockquote>
<p>小部分呢？我不好说。</p>
</blockquote>
<p><strong>无论何时，lr均是获取装备的重要来源</strong></p>
<p>本篇将会介绍新旧两种lr方式</p>
<h2 id="传统lootrun" tabindex="-1"><a class="header-anchor" href="#传统lootrun" aria-hidden="true">#</a> 传统Lootrun</h2>
