<h3 id="节日饰品" tabindex="-1"><a class="header-anchor" href="#节日饰品" aria-hidden="true">#</a> 节日饰品</h3>
<p>在每次节日期间，都会推出期间限定的Crates(饰品箱)</p>
<p>其中，一定能开出节日限定的饰品(包括但不限于：宠物、武器皮肤、头饰等等)</p>
<p>同时，节日限定的饰品没有Common(白色)品质</p>
<h3 id="如何白嫖饰品箱" tabindex="-1"><a class="header-anchor" href="#如何白嫖饰品箱" aria-hidden="true">#</a> 如何白嫖饰品箱</h3>
<p>在节日期间，每天第一次完成Daily Obj时，会额外赠送一个T1的饰品箱</p>
<p>如果你当前有银牛订阅状态，将额外赠送一个</p>
<p>当你完成Weekly Obj时，会额外赠送一个T2的饰品箱</p>
<p>同理，有银牛订阅会额外赠送一个</p>
<div class="hint-container tip">
<p class="hint-container-title">如何把白嫖精神发挥到极致？</p>
<p>通常来说，活动将于北京时间周六凌晨两点/三点开启(根据老外冬夏令时来决定)</p>
<p>那么，只要你能熬到活动开启后，在下一个每日任务刷新前，马上做完每日任务，就能多拿到一个箱子</p>
<p>同理，在活动开启当周，憋着不做每周任务，就可以在活动开启后多白嫖到T2箱子</p>
</div>
