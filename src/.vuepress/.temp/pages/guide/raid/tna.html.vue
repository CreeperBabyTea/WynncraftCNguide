<h3 id="twisted-jungle" tabindex="-1"><a class="header-anchor" href="#twisted-jungle" aria-hidden="true">#</a> Twisted Jungle</h3>
<p>玩家会被传送至一个封闭的传送门(其实是一个大虚空洞，为了区分叫它传送门)前，左右两侧共有四个通道，身后则是会固定刷怪的大大小小的虚空洞，在这一面，玩家需要保护传送门不被击破，并收集到五个虚空物质打开传送门。</p>
<p>正对传送门，从左到右四条支路分别为：跑酷，Miniboss，跑酷，Miniboss</p>
<p>左侧第一个跑酷支线，需要玩家从左右两侧分别跑到对面，激活虚空晶体后，回到支线中央，进入虚空洞取回虚空物质</p>
<p>左侧第二个miniboss支线，需要玩家击杀miniboss即可获取虚空物质</p>
<p>右侧第三个跑酷支线机制很简单，不多赘述</p>
<p>右侧第四个miniboss支线，会有两只头部是虚空洞的怪物，距离起点较远的那只携带虚空物质，需注意，一旦玩家接触到二者当中的任意一只，都会被立刻传送至另一只面前，就像进入了虚空洞一样，且会受到大幅伤害。</p>
<p>场地正中央的虚空洞会不断出现小怪来袭击传送门，其中的蓝名怪会攻击大传送门，传送门只有十点血量，如果传送门碎了就会立刻失败。</p>
<p>第五个虚空物质则会在这些刷新的小怪中出现，在第三波怪物来袭时，会出现一只头部是虚空洞的miniboss，击杀之即可获得最后一个虚空物质，打开传送门离开这。</p>
<h2 id="boss" tabindex="-1"><a class="header-anchor" href="#boss" aria-hidden="true">#</a> Boss</h2>
<p>终章的Boss——The Nameless Anomaly（后称greg）是一个非常强大的存在，而与它的对战会在一个巨大的矩形战斗盘中进行，greg拥有非常多的能力，并且拥有随着血量降低而逐渐增加的移速，需要玩家有足够的灵活性才能成功将其击败。</p>
<p>gerg同时只会瞄准一名玩家，但如果让他的仇恨在多个玩家之间跳转并不是什么好选择，推荐的做法是所有玩家从同一个方向，一边后退一边往BOSS脸上甩技能，或者由一名嘲讽战士拉住仇恨，其他玩家集火输出。</p>
<p>BOSS技能众多，但对于一般打法在平时需要特别注意的只有四个技能：</p>
<p>1.听到龙吼声时，greg会在短暂延迟后跳跃到空中，并迅速落到玩家头顶，造成大量伤害并在地上生成一个大虚空洞，不注意躲避很容易被秒杀。</p>
<p>2.听到“当——当——当——&quot;数次连续的响声，greg会在短暂延迟后进行连续冲刺，造成比上一个技能略少的伤害，并产生一连串虚空洞。<br>
3.greg向四周喷洒出一圈紫色方块，短暂延迟后爆炸，造成大量伤害。</p>
<p>4.greg释放一群头颅，头颅优先追踪不在地面上的玩家，造成固定伤害+一定比例的真实伤害。</p>
<p>其他技能具体细节可以在wiki上查询，此处只做简要教学。</p>
<p><em><strong>special stage</strong></em><br>
当greg的血量降低至75%/50%/25%后，其会<strong>在数秒延迟后</strong>释放一群主动追击玩家的眼球，对周围的玩家施加短暂失明，并传送至场地中间进入无敌，随后每隔两秒对场地上的随机玩家释放眼球激光。</p>
<p>对于召唤出的眼球——确保自己的移速够高，就能规避眼球的攻击。</p>
<p>对于眼球激光——当一名玩家被瞄准后，他的屏幕中心会出现提示——you are being watched。</p>
<p>一秒后，greg会向其瞄准的位置发射一道眼球激光，<strong>对半径八格内的所有玩家造成其最大生命值25%的真实伤害，对半径六格内的所有玩家</strong><em><strong>额外造成</strong></em><strong>其30%最大生命值的真实伤害</strong>。</p>
<p>在这一阶段，玩家需要有足够的灵活性来规避greg的伤害，且需要避免聚集扎堆。</p>
<p>greg在进入这一阶段时血量越低，这一阶段持续时间越长。</p>
