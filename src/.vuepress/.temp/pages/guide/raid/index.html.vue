<p>每个raid的主流程均有四步：三面从二选一的挑战中随机出现的一面，以及最后的BOSS面。</p>
<p>每通过一个挑战，都可以在两个挑战之间选择一个BUFF，每个raid均有五种不同类型的buff。</p>
<p>打过BOSS面之后，可以领取各个raid对应的奖励箱，其中会有绿宝石，符文(Nii,Uth,Tol)，三级以上的各类元素粉末，部分三星采集材料，以及各个raid的专属装备和Tome。</p>
<p>Raid中的死亡并不会丢失灵魂点，且您会在这一面结束时重生。</p>
<p>如果队伍中仅剩一名玩家存活，则会判定Raid失败。</p>
<p>注意：每个raid均有自己的解锁条件，会在各个raid中单独说明。</p>
