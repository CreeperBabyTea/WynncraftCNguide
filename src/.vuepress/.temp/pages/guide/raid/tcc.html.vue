<h2 id="room1" tabindex="-1"><a class="header-anchor" href="#room1" aria-hidden="true">#</a> Room1</h2>
<h3 id="minigame" tabindex="-1"><a class="header-anchor" href="#minigame" aria-hidden="true">#</a> Minigame</h3>
<p>这一面，玩家会被传送至一个封闭空间内，周围会有五个被巨石封住的道路，周围的五个通道每次会开启三个，完成通道内的挑战即可获得资源，将资源提交至中央的洞穴上，提交十个资源后洞穴开启，进入下一面。</p>
<p>五个通道的挑战分别为：</p>
<p>滚石，躲避滚石到达通道另一侧离开，离开时获得资源，不限制同时进入的人数。</p>
<p>跑酷，有左右移动的平台的小跑酷，从终点离开时获得资源，不限制同时进入的人数。</p>
<p>miniboss，同时只能进入一个玩家的通道，没什么好讲的。</p>
<p>色盲派对，玩过这个小游戏的都知道规则，没什么好讲的。这一面最多可以同时进入两个玩家，当完成挑战时如果是唯一处于这个挑战内的玩家，则会获得两份资源。</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>这一面只要卡身位在入口平台和游戏区域的交界处即可挂机通关。</p>
</div>
<h3 id="lava-lake" tabindex="-1"><a class="header-anchor" href="#lava-lake" aria-hidden="true">#</a> Lava lake</h3>
<p><strong>这一面内，机制十分重要</strong><br>
这一面，玩家会被传送至一岩浆湖的一侧，周围有一滩水和五个岩浆柱。</p>
<p>首先，玩家需要在不被小怪打死的情况下，从水潭中收集水，之后找到正在喷发的岩浆柱，与其互动将其熄灭。</p>
<p>重复五次后，第一块平台将会升起，此时，需要一名玩家站立在其上并保持存活，否则平台会消失。</p>
<p>其余玩家重复上述操作，第二个平台升起后，刚刚站在第一块平台上的玩家需移动到第二块平台，并且第一块平台会消失。</p>
<p>当第三块平台升起后，挑战完成。</p>
<p>如果您不慎掉落入熔岩内，您会在出生点重生。</p>
<h2 id="room2" tabindex="-1"><a class="header-anchor" href="#room2" aria-hidden="true">#</a> Room2</h2>
<h3 id="maze" tabindex="-1"><a class="header-anchor" href="#maze" aria-hidden="true">#</a> Maze</h3>
<p>玩家会被传送至一个迷宫中心，周围共有四条路——红橙绿蓝。</p>
<p>地图中心有一个翅膀，队伍中有一名玩家需要拾起翅膀，以上帝视角遍历整个迷宫，为队友找到终点。</p>
<p>迷宫终点由紫色和白色的粒子组成的圈进行标记，<strong>请确保您开启了粒子效果</strong>。</p>
<div class="hint-container warning">
<p class="hint-container-title">注意</p>
<p>处于上帝视角的玩家视为已死亡，请注意raid失败条件。</p>
</div>
<p>当上帝视角的玩家找到终点，其需要为其他队友引路，迷宫内会有多个封闭的门，其余玩家需要与门交互并击杀召唤出的miniboss才能继续前进。</p>
<h3 id="platform" tabindex="-1"><a class="header-anchor" href="#platform" aria-hidden="true">#</a> Platform</h3>
<p><strong>这一面内，机制十分重要</strong><br>
<strong>这一面，只要有超过一名玩家阵亡，raid就会失败</strong></p>
<p>玩家会被传送至一个小山谷内，四周会有五条不同颜色标记的道路。数秒后，会有三条路开启，玩家必须在五秒内分散到三条路中，否则需要返回中央重复此操作。</p>
<p>五秒后，道路关闭，各个道路内的玩家需要站在面前的平台上，仅当三个平台都被激活时，倒计时才会启动。</p>
<p>30s后，平台被禁用，玩家需要回到场地中间重复该操作，当倒计时(60s)结束后，挑战通过，玩家可以进入下一面。</p>
<h2 id="room3" tabindex="-1"><a class="header-anchor" href="#room3" aria-hidden="true">#</a> Room3</h2>
<h3 id="statue" tabindex="-1"><a class="header-anchor" href="#statue" aria-hidden="true">#</a> Statue</h3>
<p>玩家会被传送至一个大空间内，空间的东南北三个方向各有一个雕像，雕像共有六点生命。</p>
<p>当玩家进入该场景，120s倒计时将会被激活，之后每隔10s，场地内会刷出大量敌怪，其中，僵尸外观的敌怪会攻击雕像，需要尽快解决。<br>
如果任意雕像血量降低到0，则挑战重启。</p>
<p>倒计时结束后，挑战通过</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>当前倒计时剩余时间&lt;=最低血量的雕像血量*10时，雕像是绝对安全的，请优先保证自己不要死亡。</p>
</div>
<h3 id="platform-1" tabindex="-1"><a class="header-anchor" href="#platform-1" aria-hidden="true">#</a> Platform</h3>
<p>玩家会被传送至一座山前，山脚下的路是被封住的，山顶上有一平台。</p>
<p>当有玩家踩在山顶平台上时，山下的通道会打开，山下内部也有一个平台。</p>
<p>山顶和山脚的平台各有60s的倒计时山下的倒计时用于开启山顶的通道，山顶的倒计时用于开启终点，倒计时均结束后，方可挑战成功。<br>
需注意，周围会持续刷怪，平台每隔一段时间会启动防御模式，弹飞站在平台上的玩家，请保护好自己。</p>
<div class="hint-container tip">
<p class="hint-container-title">提示</p>
<p>站在平台中间3*3范围的角落上，可以规避平台防御模式的攻击。</p>
</div>
<h2 id="boss" tabindex="-1"><a class="header-anchor" href="#boss" aria-hidden="true">#</a> Boss</h2>
<p>TCC的BOSS共有三个阶段，机制各不相同 。</p>
<p>请注意，tcc的boss巨像会使用大量散弹攻击，生存能力不高的build很容易在TCC的BOSS面暴毙。</p>
<p>BOSS站会在多个浮空的平台上进行，如果您不慎跌落，您会失去当前生命值的50%并回到当前阶段的起始点。</p>
<p><em><strong>stage1:</strong></em><br>
巨像双手持剑挡在身前，轮流破坏其左右手即可进入下一阶段。</p>
<p><em><strong>stage2:</strong></em><br>
巨像将玩家抬到面前，玩家需要攻击巨像的两个眼睛和嘴共三个目标。</p>
<p>注意：巨像的任意眼睛降到半血以下时，会召唤一个能量核心，玩家必须击败能量核心，击败能量核心的玩家会被标记(继续读)。</p>
<p>解决第二阶段后，巨像的头会滚落下来，并将所有玩家丢出战斗盘。</p>
<p><em><strong>stage3:</strong></em><br>
玩家被甩到巨像的头前，需要击败巨像的嘴、两只耳朵、后脖颈四个目标。</p>
<p>刚进入这一面时，玩家会被强制传送到巨像嘴边，并且巨像之嘴会发动技能，**请在upcomming attack计时结束之前躲开，该技能伤害极高。**如果您跌落到平台下，您会被传送到巨像的后脖颈处。</p>
<p>巨像的嘴会根据玩家的攻击次数进行伤害反射，请额外注意这一点。</p>
<p>在四个部分都被击败后，巨像脖子处的入口将会开启，<strong>在第二阶段内被标记的玩家需进入巨像内部完成解密，因此，第二阶段被标记的玩家请务必保护自己，不要在第三阶段阵亡</strong>。</p>
<p>解密内容是根据地板上的图案按下墙上对应位置的按钮。</p>
