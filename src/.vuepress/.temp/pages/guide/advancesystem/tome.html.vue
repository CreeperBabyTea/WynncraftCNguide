<ul>
<li>
<p>红色：武器tome槽<br>
武器tome会最终加算你对怪物造成的伤害，同时给予部分攻击相关的属性，可装备两个</p>
<blockquote>
<p>武器tome分为三种品质，legendary, fabled, Mythic，其中对于游戏末期来说，仅有后两种有用，且Mythic品质的tome出现概率非常低<br>
notg产出的武器tome属性并不高，这里不多赘述<br>
nol产出的武器tome分别提供8%(fabled)和12%(Mythic)的额外伤害加成，并额外提供3单一属性点(fabled)/全属性点各一(fabled)/2%-10%单一元素伤害加成(Mythic)/2%-8%全元素伤害加成(Mythic)<br>
tna产出的武器tome分别提供13%(fabled)和16%(Mythic)的额外伤害加成，并额外提供3单一属性点(fabled)/全属性点各一(fabled)/1mr或者1ms(fabled)/2%-10%单一元素伤害加成(Mythic)/2%-8%全元素伤害加成(Mythic)</p>
</blockquote>
</li>
<li>
<p>紫色：杀敌经验加成tome槽<br>
杀敌经验加成顾名思义，会增加你从怪物身上获得的战斗经验，可装备两个</p>
<blockquote>
<p>notg/nol&amp;tcc/tna分别产出一/二/三级Slaying tome，分别提供15%/30%/45%额外杀敌经验加成</p>
</blockquote>
</li>
<li>
<p>黄色：防具tome槽<br>
防具tome会最终加算怪物对你造成的减伤，同时给予部分辅助相关的属性，可装备四个</p>
<blockquote>
<p>notg产出的防具tome属性并不高，这里不多赘述<br>
tcc产出的防具tome分别提供4%(fabled)和5%(Mythic)的基础抗性加成，该抗性与职业本身的抗性特质分别计算而非简单加算<br>
除此之外，fabled品质的防具tome会拥有以下副词条之一：<br>
2%-8% Main attack dmg + 2%-8% Exploding<br>
2%-10% Throns + 2%-10% Reflection<br>
2%-8% Spell dmg<br>
2%-8% Loot bonus<br>
2%-8% Walk speed<br>
29-65 Hpr(注：可能不准确)<br>
24-100 life steal(注：可能不准确)<br>
Mythic品质的防具tome会拥有以下两条属性：<br>
100-286 HP (注：最低值可能不准确但最高值是准确数据)<br>
2%-10% 随机单一元素防御，或2%-8%全元素防御</p>
