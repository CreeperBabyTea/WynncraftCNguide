<div class="hint-container tip">
<p class="hint-container-title">tome来源</p>
<p>大部分强力tome的来源均为<RouterLink to="/guide/raid.html">RAID</RouterLink></p>
<p>notg会产出1级的防具tome和武器tome(品质最低为legendary 最高fabled)</p>
<p>tcc会产出2级的防具tome(品质最低为fabled 最高Mythic)</p>
<p>nol会产出2级的武器tome(品质最低为fabled 最高Mythic)</p>
<p>tna会产出3级的武器tome(品质最低为fabled 最高Mythic)</p>
<p>Slaying tome, Dungeoneering tome, Gathering tome只有legenday稀有度，并且一级来源于notg，二级来源于nol或tcc，三级来源于tna</p>
<p>这三种tome都没有额外属性，仅提供特定的经验加成，并且等级越高效果越强，通常不需要低级的</p>
<p>目前的版本来说，毕业防具tome和武器tome分别需要在tcc和tna刷取</p>
<p>公会tome需要在公会系统中由有权限的成员给予</p>
<p>lootrun tome需要在lr中获取</p>
