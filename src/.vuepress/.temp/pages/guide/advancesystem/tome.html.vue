<h3 id="公会tome-guild-mastery" tabindex="-1"><a class="header-anchor" href="#公会tome-guild-mastery" aria-hidden="true">#</a> 公会tome(Guild Mastery)</h3>
<p>公会tome会给予一定的属性点数，可装备一个</p>
<p>公会tome只有Legendary品质，词条为下列中的随机一个:</p>
<ul>
<li>提供3点单一属性点+2%-10%该属性伤害</li>
