<p><strong>通天塔</strong></p>
<p>这个任务是对玩家战斗技能的一个挑战，仅有战斗元素。战斗难度随任务进行而增加。</p>
<p>任务全程在通天塔进行，共7层，每层10个房间。</p>
<p>任务开始的方法是和塔底层的任务NPC讲话，之后就可以去第一层，打怪。</p>
<p>小怪关卡的目标是杀死10个小怪，捡起10掉落物，并交给那个房间里的NPC，然后走到他后面，进去下一个房间。</p>
<p>boss关卡的目标是杀死boss，捡起掉落物，并交给那个房间里的NPC，然后走到他后面，完成此层。</p>
<p><strong>本任务的设计是不能让玩家在开始任务等级完成的。</strong></p>
<p>尤其因为最终boss过于强大，且旁边有其他层的boss的保护</p>
